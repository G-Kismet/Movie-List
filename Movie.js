class Movie {
   constructor(title, director, actors, releaseDate, synopsis, genre, accolades, rating){
   this.title = title;
   this.director = director;
   this.actors = actors;
   this.releaseDate = releaseDate;
   this.synopsis = synopsis;
   this.genre = genre;
   this.accolades = accolades;
   this.rating = rating;
   }
}




export default Movie